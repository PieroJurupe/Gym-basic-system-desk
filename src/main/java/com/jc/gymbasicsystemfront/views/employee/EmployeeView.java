    private void initTable() {
        tableModelEmployee = new DefaultTableModel();
        tableModelEmployee.addColumn("Nombres");
        tableModelEmployee.addColumn("Apellidos");
        tableModelEmployee.addColumn("Correo");
        tableModelEmployee.addColumn("Numero de telefono");
        tableModelEmployee.addColumn("Direccion");
        tblEmployees.setModel(tableModelEmployee);
    }

    private void loadEmployeesData() {

        try {
            List<EmployeeDto> employees = employeeService.getAll();

            for (EmployeeDto employee : employees) {
                tableModelEmployee.addRow(new Object[]{
                    employee.getFirstName(),
                    employee.getLastName(),
                    employee.getEmail(),
                    employee.getPhoneNumber(),
                    employee.getAddress()
                });
            }
        } catch (CustomServiceException e) {
            System.out.println(e.getMessage());
        }

    }

    public void refreshTblEmployeeData() {
        tableModelEmployee.setRowCount(0);
        loadEmployeesData();
    }
    
